#include "libft.h"

int	ft_atoi(const char *str)
{
	int			sign;
	long int	nb;
	size_t		i;

	i = 0;
	nb = 0;
	sign = 1;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = sign * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - 48);
		i++;
		if (nb * sign < INT_MIN)
			return (0);
		if (nb * sign > INT_MAX)
			return (-1);
	}
	return ((size_t) sign * (size_t)nb);
}
